export function getSubject(id: string): Subject | undefined {
  return curriculum.find((s) => s.id === id);
}

export function getTopic(subjectId: string, topicId: string): Topic | undefined {
  const subject = getSubject(subjectId);
  return subject?.topics.find((t) => t.id === topicId);
}

export function getAllTopics(): { subject: Subject; topic: Topic }[] {
  return curriculum.flatMap((subject) =>
    subject.topics.map((topic) => ({ subject, topic }))
  );
}
